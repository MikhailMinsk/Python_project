def morse_bits(bits):
    return bits.replace('00000000000000', '  ') \
        .replace('111111', '-') \
        .replace('11111', '-') \
        .replace('111', '-') \
        .replace('11', '.') \
        .replace('1', '.') \
        .replace('000000000', '') \
        .replace('00000000', '') \
        .replace('000000', '  ') \
        .replace('00000', '') \
        .replace('000', ' ') \
        .replace('00', '') \
        .replace('0', '') \
        .split(' ')


